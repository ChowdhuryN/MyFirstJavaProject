package Assignment_02;

public class unary {

    public static void main(String[] args) {
        int num1 = 40;
        int num2=60;
        //unary operator
        System.out.println("num1:"+ num1);
        System.out.println("num2:"+ num2);

        //increment operator
        System.out.println("Increment operator");
        System.out.println("num1++:"+ num1++);
        System.out.println("++num2:" + ++num2);

        //decrement operator
        System.out.println("Decrement operator");
        System.out.println("num1--:" + num1--);
        System.out.println("--num2:" + --num2);

        System.out.println("num1:" + num1);
        System.out.println("num2:" + num2);
    }
}
